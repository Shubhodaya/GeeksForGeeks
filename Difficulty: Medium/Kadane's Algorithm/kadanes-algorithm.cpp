class Solution {
  public:
    // Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(vector<int> &arr) {
        // code here...
        
        long long s=0;
        long long maxi=-1e9,sum=-1e9;
        
        for( int i=0;i<arr.size();i++){
            maxi=max(maxi,(long long)arr[i]);
            s+=arr[i];
            sum=max(sum,s);
            if(s<0)s=0;
            
        }
        
        return max(maxi,sum);
    }
};


class Solution {
  public:
    int rectanglesInCircle(int r) {
        // code here
        int ans=0;
        int k=(2*r) -1;
        for(int i=1;i<=k;i++){
            for(int j=1;j<=k;j++){
                if(sqrt(i*i + j*j)<=k+1)ans++;
            }
        }
        
        return ans;
    }
};

